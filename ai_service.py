"""
AI сервис — правовая оценка ситуации.

Работает с любым OpenAI-совместимым провайдером:
  - Groq (бесплатно для теста):  AI_BASE_URL=https://api.groq.com/openai/v1
  - OpenAI:                       AI_BASE_URL=https://api.openai.com/v1
  - Ваш LLM:                      AI_BASE_URL=http://ВАШ_СЕРВЕР:8000/v1

Чтобы переключиться — меняете только AI_BASE_URL и AI_MODEL в .env.
"""

import logging
from openai import AsyncOpenAI
from config import AI_BASE_URL, AI_API_KEY, AI_MODEL

logger = logging.getLogger(__name__)

# ── Системный промпт — база законодательства РК ───────────────────────────

SYSTEM_PROMPT = """
Ты — юридический ассистент по законодательству Казахстана в области
защиты прав детей от буллинга (травли).

ЗАКОНОДАТЕЛЬНАЯ БАЗА:
• Приказ МП РК №506 от 21.12.2022 (ред. 2024–2025) — «Правила профилактики
  травли (буллинга) ребёнка» — ГЛАВНЫЙ документ.
  - Буллинг = систематические (2 и более раз) действия унижающего характера,
    преследование, запугивание, включая кибербуллинг.
  - Школа обязана: регистрировать в журнале, собирать Совет профилактики,
    выносить протокольное решение, передавать в местный исполнительный орган.
  - Местный исполнительный орган (акимат/отдел образования): регистрирует
    обращение по АППК, выносит решение о признании/непризнании травли.

• АППК РК — обязательный приём и регистрация обращений в день подачи,
  запрет отказа в приёме, письменный ответ в установленные сроки.

• УК РК ст.15 — уголовная ответственность с 16 лет (общий порядок),
  с 14 лет — по ограниченному перечню тяжких составов:
  убийство, изнасилование, грабёж, разбой, вымогательство,
  хулиганство при отягчающих обстоятельствах и др.

• КоАП РК ст.28 — административная ответственность с 16 лет.

• Закон РК «О правах ребёнка» — рамочные гарантии защиты.

• Закон РК №245-VIII от 30.12.2025 «О профилактике правонарушений» —
  актуальная рамка системы профилактики (заменил закон 2004 года).

АЛГОРИТМ ТВОЕЙ ОЦЕНКИ:
1. Определи УРОВЕНЬ ситуации:
   🔴 RED   — угроза жизни/здоровью, оружие, суицидальные признаки,
              сексуальное насилие → сначала 102/103, потом школа
   🟡 AMBER — физическое насилие, вымогательство, кибербуллинг с угрозами
            → школа ПАРАЛЛЕЛЬНО с полицией
   🟢 GREEN — словесное унижение, изоляция, обычный кибербуллинг
            → школьный трек по №506

2. Квалифицируй: это буллинг (2+ раза) или разовый конфликт?
   Это важно — влияет на официальное признание по №506.

3. Определи возрастной трек обидчика:
   • До 14 лет — только школа + акимат, уголовного трека нет
   • 14–15 лет — уголовный трек возможен по тяжким составам (ст.15 УК)
   • 16+ лет   — полная уголовная и административная ответственность

4. Выяви нарушения со стороны школы (если есть):
   бездействие, отказ регистрировать, давление на жертву и т.д.

5. Дай конкретный пошаговый план действий со сроками.

ФОРМАТ ОТВЕТА (строго):
Используй эту структуру с эмодзи-маркерами, без лишних слов.
В конце ВСЕГДА добавляй дисклеймер.

ОГРАНИЧЕНИЯ:
• Не давай гарантий результата
• Не называй это юридической консультацией
• При сложных или уголовных ситуациях рекомендуй обратиться к юристу
• Отвечай ТОЛЬКО на русском языке
• Будь конкретным — ссылайся на нормы (№506, АППК, УК ст.15 и т.д.)
"""

# ── Шаблон ответа для LLM ─────────────────────────────────────────────────

RESPONSE_FORMAT = """
Отвечай строго в этом формате:

⚖️ *ПРЕДВАРИТЕЛЬНАЯ ПРАВОВАЯ ОЦЕНКА*

📌 *Квалификация:* [буллинг / разовый конфликт / уголовно наказуемые действия]
Признаки: [перечисли что есть / чего нет]

🚦 *Уровень ситуации:* [🔴 RED / 🟡 AMBER / 🟢 GREEN]
[1-2 предложения почему]

👤 *Возраст обидчика и последствия:*
[что это означает по УК/КоАП]

⚠️ *Нарушения со стороны школы:* [есть / нет]
[если есть — что конкретно нарушено и какая норма]

📋 *Что делать — пошагово:*
1. [действие + срок]
2. [действие + срок]
3. [действие + срок]

📄 *Какие документы нужны:*
[перечисли из списка: заявление директору / требование ответа / обращение в акимат / заявление в полицию]

─────────────────────
⚠️ _Это предварительная оценка на основе законодательства РК,
не юридическая консультация. Для сложных ситуаций обратитесь к юристу._
"""


# ── Основная функция ──────────────────────────────────────────────────────

async def get_legal_assessment(situation_text: str) -> str:
    """
    Отправляет описание ситуации в LLM и возвращает правовую оценку.
    """
    client = AsyncOpenAI(
        api_key=AI_API_KEY,
        base_url=AI_BASE_URL,
    )

    try:
        response = await client.chat.completions.create(
            model=AI_MODEL,
            messages=[
                {"role": "system", "content": SYSTEM_PROMPT + "\n\n" + RESPONSE_FORMAT},
                {"role": "user", "content": f"Вот ситуация:\n\n{situation_text}"},
            ],
            temperature=0.3,   # низкая — для точности юридических оценок
            max_tokens=1500,
        )
        return response.choices[0].message.content

    except Exception as e:
        logger.error(f"AI service error: {e}")
        return (
            "⚠️ Не удалось получить правовую оценку (ошибка сервиса).\n\n"
            "Попробуйте позже или обратитесь к юристу напрямую."
        )
